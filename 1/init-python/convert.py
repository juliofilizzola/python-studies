# Convers達o de int para float
inteiro = 10
ponto_flutuante = float(inteiro)
print(f"Inteiro: {inteiro}, Ponto Flutuante: {ponto_flutuante}")

print('===============================================')

# convers達o de float para int

ponto_flutuante = 5.9
inteiro = int(ponto_flutuante)
print(f"Ponto Flutuante: {ponto_flutuante}, Inteiro: {inteiro}")

print('===============================================')

# convers達o de int para str

inteiro = 42
texto = str(inteiro)
print(f"Inteiro: {inteiro}, Texto: {texto}")

print('===============================================')

# convers達o de str em float

texto = "3.14"
ponto_flutuante = float(texto)
print(f"Texto: {texto}, Ponto Flutuante: {ponto_flutuante}")
